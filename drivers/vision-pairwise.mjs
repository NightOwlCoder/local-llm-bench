#!/usr/bin/env node
// Pairwise UI polish comparison using Claude Opus 4.7 via Bedrock.
// Takes two screenshots + context, returns winner + delta + reasoning.
//
// Usage: node drivers/vision-pairwise.mjs <image-a.png> <image-b.png> <benchmark>
//
// Requires: AWS credentials in env (AWS_PROFILE or AWS_ACCESS_KEY_ID/etc)

import { BedrockRuntimeClient, InvokeModelCommand } from '@aws-sdk/client-bedrock-runtime';
import { readFileSync } from 'fs';
import { basename, resolve } from 'path';

const [, , imgAArg, imgBArg, benchmark] = process.argv;
if (!imgAArg || !imgBArg || !benchmark) {
  console.error('usage: vision-pairwise.mjs <a.png> <b.png> <benchmark>');
  process.exit(2);
}

const imgAPath = resolve(imgAArg);
const imgBPath = resolve(imgBArg);
const imgA = readFileSync(imgAPath).toString('base64');
const imgB = readFileSync(imgBPath).toString('base64');

const rubric = `You are judging two web app UIs generated by different LLMs for the same prompt. Both apps are ${benchmark}s.

Compare them on modern visual polish. Consider:
- Typography (hierarchy, weight, line-height)
- Color use (palette cohesion, contrast, accent colors)
- Spacing and layout discipline
- Depth signals (shadows, gradients, textures, grid effects)
- Rounded corners, refined edges
- Syntax highlighting (for text/code apps)
- Overall "2026 web app" vibe vs bare-bones / dev-tool feel
- Which feels like a product vs which feels like a first draft

Ignore minor functional differences — focus on visual craft.

Respond ONLY in valid JSON (no markdown fences, no prose outside JSON):
{
  "winner": "A" | "B" | "tie",
  "delta": 0-3,
  "score_a": 0-5,
  "score_b": 0-5,
  "reason_a": "one sentence on image A",
  "reason_b": "one sentence on image B",
  "verdict": "one sentence on why the winner wins"
}

"delta" is how decisive the win is: 0=tie, 1=slight, 2=clear, 3=landslide.`;

const body = {
  anthropic_version: 'bedrock-2023-05-31',
  max_tokens: 800,
  messages: [
    {
      role: 'user',
      content: [
        { type: 'text', text: `Image A (${basename(imgAArg)}):` },
        { type: 'image', source: { type: 'base64', media_type: 'image/png', data: imgA } },
        { type: 'text', text: `Image B (${basename(imgBArg)}):` },
        { type: 'image', source: { type: 'base64', media_type: 'image/png', data: imgB } },
        { type: 'text', text: rubric },
      ],
    },
  ],
};

// Region is us-east-1 per ilha config for opus-4.7 global inference profile
const client = new BedrockRuntimeClient({ region: 'us-east-1' });
const modelId = 'global.anthropic.claude-opus-4-7';

try {
  const resp = await client.send(new InvokeModelCommand({
    modelId,
    contentType: 'application/json',
    accept: 'application/json',
    body: JSON.stringify(body),
  }));
  const decoded = JSON.parse(new TextDecoder().decode(resp.body));
  const raw = decoded.content?.[0]?.text?.trim() || '';
  let json;
  const fenced = raw.match(/```(?:json)?\s*([\s\S]*?)```/);
  const candidate = fenced ? fenced[1].trim() : raw;
  try {
    json = JSON.parse(candidate);
  } catch {
    const m = candidate.match(/\{[\s\S]*\}/);
    if (m) json = JSON.parse(m[0]);
    else throw new Error(`could not parse: ${raw.slice(0, 300)}`);
  }
  console.log(JSON.stringify({
    image_a: basename(imgAArg),
    image_b: basename(imgBArg),
    benchmark,
    ...json,
    _model: modelId,
    _usage: decoded.usage || null,
  }, null, 2));
} catch (err) {
  console.error(`fatal: ${err.name}: ${err.message}`);
  process.exit(5);
}
