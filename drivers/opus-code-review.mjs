#!/usr/bin/env node
// Opus code review for pygame artifacts.
// Scores correctness, style, bugs, architecture.
//
// Usage: node drivers/opus-code-review.mjs <artifact.py> <model-safe-name> <results-dir>

import { BedrockRuntimeClient, InvokeModelCommand } from '@aws-sdk/client-bedrock-runtime';
import { readFileSync, writeFileSync } from 'fs';
import { resolve, join } from 'path';

const [, , artifactArg, safeName, resultsDir] = process.argv;
if (!artifactArg || !safeName || !resultsDir) {
  console.error('usage: opus-code-review.mjs <artifact.py> <safe-name> <results-dir>');
  process.exit(2);
}

const artifact = resolve(artifactArg);
const src = readFileSync(artifact, 'utf8');

const rubric = `You are doing a code review on a Python pygame snake game generated by an LLM. Score it on multiple dimensions.

Scoring rubric (each 0-10, integer only):
- correctness: does the code work? (syntax, logic, event handling, game loop)
- style: readability, naming, organization, comments where needed (not noise)
- architecture: separation of concerns, functions vs globals, magic numbers
- robustness: edge cases, error handling, proper cleanup
- ux: playability — controls, game over handling, restart, score display, speed

Return ONLY valid JSON (no markdown fences, no prose):
{
  "correctness": N,
  "style": N,
  "architecture": N,
  "robustness": N,
  "ux": N,
  "total": N (sum of above, 0-50),
  "notes": "2-3 sentences on the most notable strengths/weaknesses"
}`;

const body = {
  anthropic_version: 'bedrock-2023-05-31',
  max_tokens: 1000,
  messages: [
    {
      role: 'user',
      content: [
        { type: 'text', text: rubric },
        { type: 'text', text: `\n\nCode to review:\n\n\`\`\`python\n${src}\n\`\`\`` },
      ],
    },
  ],
};

const client = new BedrockRuntimeClient({ region: 'us-east-1' });
const modelId = 'global.anthropic.claude-opus-4-7';

try {
  const resp = await client.send(new InvokeModelCommand({
    modelId,
    contentType: 'application/json',
    accept: 'application/json',
    body: JSON.stringify(body),
  }));
  const decoded = JSON.parse(new TextDecoder().decode(resp.body));
  const raw = decoded.content?.[0]?.text?.trim() || '';
  const fenced = raw.match(/```(?:json)?\s*([\s\S]*?)```/);
  const candidate = fenced ? fenced[1].trim() : raw;
  let json;
  try {
    json = JSON.parse(candidate);
  } catch {
    const m = candidate.match(/\{[\s\S]*\}/);
    if (m) json = JSON.parse(m[0]);
    else throw new Error(`could not parse: ${raw.slice(0, 300)}`);
  }
  const out = {
    model: safeName.replace(/--/g, ':'),
    benchmark: 'snake-pygame-review',
    artifact: artifactArg.includes("/output/") ? "output/" + artifactArg.split("/output/")[1] : artifactArg.split("/").pop(),
    ...json,
    _model: modelId,
    _usage: decoded.usage || null,
  };
  writeFileSync(join(resultsDir, `snake-pygame-review-${safeName}.json`), JSON.stringify(out, null, 2));
  console.log(`total=${json.total}/50`);
} catch (err) {
  console.error(`fatal: ${err.name}: ${err.message}`);
  process.exit(5);
}
